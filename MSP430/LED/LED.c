#include <msp430.h>
/*************************************************

 函数(模块)名称:	Init_LED();

 功能:        初始化LED

 *************************************************/

void Init_LED(void) {
	P1DIR = BIT0;  //设置P1.0为输出状态  LED1
	P1OUT &= ~BIT0;  //设置P1.0为输出低电平
	P8DIR = BIT1 + BIT2;  //设置P8.1为输出状态 LED2
	P8OUT &= ~BIT1 + ~BIT2;  //设置P8.1为输出低电平
}

/*************************************************

 函数(模块)名称:	SLED1();

 功能:        点亮LED1

 *************************************************/

void SLED1(void) {
	P1OUT |= BIT0;  //设置P1.0为输出低电平
}

/*************************************************

 函数(模块)名称:	RLED1();

 功能:        熄灭LED1

 *************************************************/

void RLED1(void) {
	P1OUT &= ~BIT0;  //设置P1.0为输出低电平
}

/*************************************************

 函数(模块)名称:	SLED2();

 功能:        点亮LED2

 *************************************************/

void SLED2(void) {
	P8OUT |= BIT1;  //设置P1.0为输出低电平
}

/*************************************************

 函数(模块)名称:	RLED2();

 功能:        熄灭LED2

 *************************************************/

void RLED2(void) {
	P8OUT &= ~BIT1;  //设置P1.0为输出低电平
}
/*************************************************

 函数(模块)名称:	SLED3();

 功能:        点亮LED3

 *************************************************/

void SLED3(void) {
	P8OUT |= BIT2;  //设置P1.0为输出低电平
}

/*************************************************

 函数(模块)名称:	RLED3();

 功能:        熄灭LED3

 *************************************************/

void RLED3(void) {
	P8OUT &= ~BIT2;  //设置P1.0为输出低电平
}

/*************************************************

 函数(模块)名称:	FLED1();

 功能:        取反LED1

 *************************************************/

void FLED1(void) {
	P1OUT ^= BIT0;  //设置P1.0为输出低电平
}

/*************************************************

 函数(模块)名称:	FLED2();

 功能:        取反LED2

 *************************************************/

void FLED2(void) {
	P8OUT ^= BIT1;  //设置P1.0为输出低电平
}

/*************************************************

 函数(模块)名称:	FLED3();

 功能:        取反LED3

 *************************************************/

void FLED3(void) {
	P8OUT ^= BIT2;  //设置P1.0为输出低电平
}




